/**
 * @author Diego
 */
public class MediaAritmetica { // Criação da classe MediaAritmetica que retorna a média aritmética de 3 notas
    /**
     *
     * @param nota1
     * @param nota2
     * @param nota3
     * @return
     */
    public double calculoMediaAritmetica(double nota1, double nota2, double nota3) {
            return nota1 + nota2 + nota3 / 3;
    };


}


